void Change_Duty_Cycle(int offset)
{
	static uint8_t direction;
	
	if(direction)
		duty -= offset;
	else
		duty += offset;
	
	if(duty>25)
	{
		direction = 1;
		duty = 25;
	}
	else if(duty < 12)
	{
		direction = 0;
		duty = 12;
	}
}

	
